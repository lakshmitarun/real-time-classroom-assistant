def generate_translations(start_id=582):
    """Generate all translations as a flat list with IDs and categories."""
    rows = []
    current_id = start_id
    
    for category, terms in TRANSLATIONS.items():
        for english, (bodo, mizo) in terms.items():
            rows.append({
                'ID': f"{current_id:04d}",
                'English': english,
                'Bodo': bodo,
                'Mizo': mizo,
                'Category': category
            })
            current_id += 1
    
    return rows, current_id

def save_to_csv(rows, output_path):
    """Save translations to CSV file."""
    fieldnames = ['ID', 'English', 'Bodo', 'Mizo', 'Category']
    
    with open(output_path, 'w', newline='', encoding='utf-8') as csvfile:
        writer = csv.DictWriter(csvfile, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)
    
    return len(rows)

def main():
    """Main function to generate and save translations."""
    output_path = r'd:\classroom-assistant\data\new_translations.csv'
    
    print("Generating translations...")
    rows, final_id = generate_translations(start_id=582)
    
    print(f"Generated {len(rows)} translation entries (IDs: 0582 to {final_id-1:04d})")
    
    count = save_to_csv(rows, output_path)
    
    print(f"✓ Saved {count} translations to: {output_path}")
    print(f"\nTo merge with your existing dataset:")
    print(f"1. Open the new CSV file: new_translations.csv")
    print(f"2. Copy all data rows (excluding header)")
    print(f"3. Append to the end of classroom_dataset_complete.csv")

if __name__ == '__main__':
    main()
