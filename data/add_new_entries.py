def get_next_id(csv_path):
    """Get the next available ID from the dataset."""
    try:
        with open(csv_path, 'r', encoding='utf-8') as f:
            lines = f.readlines()
            if len(lines) > 1:
                last_line = lines[-1].strip()
                if last_line:
                    last_id = int(last_line.split(',')[0])
                    return last_id + 1
    except Exception as e:
        print(f"Warning: {e}")
    return 1

def main():
    csv_path = r'd:\classroom-assistant\data\classroom_dataset_complete.csv'
    
    next_id = get_next_id(csv_path)
    
    print(f"Adding {len(NEW_ENTRIES)} new entries to dataset...")
    print(f"Starting ID: {next_id:04d}")
    
    # Append to CSV
    with open(csv_path, 'a', newline='', encoding='utf-8') as f:
        writer = csv.writer(f)
        
        current_id = next_id
        for english, bodo, mizo, category in NEW_ENTRIES:
            writer.writerow([f"{current_id:04d}", english, bodo, mizo, category])
            current_id += 1
    
    final_id = current_id - 1
    print(f"\n✅ Successfully added {len(NEW_ENTRIES)} new entries!")
    print(f"Final ID: {final_id:04d}")
    print(f"Total entries in dataset: {final_id}")

if __name__ == '__main__':
    main()
