def generate_all_bidirectional():
    """Generate all three translation tables"""
    
    # Table 1: English -> Bodo -> Mizo
    en_to_all = []
    # Table 2: Bodo -> English -> Mizo
    bodo_to_all = []
    # Table 3: Mizo -> English -> Bodo
    mizo_to_all = []
    
    for english, (bodo, mizo) in TRANSLATIONS.items():
        en_to_all.append((english, bodo, mizo, "Translation"))
        bodo_to_all.append((bodo, english, mizo, "Translation"))
        mizo_to_all.append((mizo, english, bodo, "Translation"))
    
    return en_to_all, bodo_to_all, mizo_to_all

def get_next_id(csv_path):
    """Get the next available ID from the dataset."""
    try:
        with open(csv_path, 'r', encoding='utf-8') as f:
            lines = f.readlines()
            if len(lines) > 1:
                last_line = lines[-1].strip()
                if last_line:
                    last_id = int(last_line.split(',')[0])
                    return last_id + 1
    except Exception as e:
        print(f"Warning: {e}")
    return 1

def merge_all_to_csv(csv_path, en_to_all, bodo_to_all, mizo_to_all):
    """Merge all three bidirectional tables into main CSV"""
    
    next_id = get_next_id(csv_path)
    
    # Append to CSV
    with open(csv_path, 'a', newline='', encoding='utf-8') as f:
        writer = csv.writer(f)
        
        current_id = next_id
        # Add English -> Bodo -> Mizo
        for english, bodo, mizo, category in en_to_all:
            writer.writerow([f"{current_id:04d}", english, bodo, mizo, category])
            current_id += 1
        
        # Add Bodo -> English -> Mizo
        for bodo, english, mizo, category in bodo_to_all:
            writer.writerow([f"{current_id:04d}", bodo, english, mizo, category])
            current_id += 1
        
        # Add Mizo -> English -> Bodo
        for mizo, english, bodo, category in mizo_to_all:
            writer.writerow([f"{current_id:04d}", mizo, english, bodo, category])
            current_id += 1
    
    return current_id - 1

